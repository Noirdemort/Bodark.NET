        public string[] PsuedoRandomPasswordGenerator(int passwordLength, int passwordCount, bool browserMode)
        {
            const string set = "qwertyuiopalskQWEPOIRUTYALSKM1230948576NJHGFDVCXZdjfhgmnbxvcz";
            const string specialSet = "qwer!t#y$u%iopalskQWEPOIRUTYALSKM123094@8576NJHGF&DVC^XZdjfhgmnbxvcz";
            string[] passwordSet = new string[passwordCount];
            RNGCryptoServiceProvider provider;
            byte[] byteArray;
            uint randomInteger;
            char x;
            for (int i = 0; i < passwordCount; i++)
            {
                string password = "";
                for (int j = 1; j <= passwordLength; j++)
                {
                    provider = new RNGCryptoServiceProvider();
                    byteArray = new byte[4];
                    provider.GetBytes(byteArray);

                    //convert 4 bytes to an integer
                    randomInteger = BitConverter.ToUInt32(byteArray, 0);

                    if (browserMode)
                    {
                        x = set[(int)((Int64)randomInteger % set.Length)];
                    }
                    else
                    {
                        x = specialSet[(int)((Int64)randomInteger % specialSet.Length)];
                    }

                    password += x;
                    if (browserMode && j % 4 == 0 && j != passwordLength)
                    {
                        password += '-';
                    }
                }
                passwordSet[i] = password;
            }
            return passwordSet;
        }

        private List<uint> FoxSat()
        {
            RNGCryptoServiceProvider provider;
            byte[] byteArray;
            uint randomInteger;
            List<uint> wordIndexes = new List<uint>();
            uint temp = 0;

            for (int i = 1; i <= 25; i++)
            {
                provider = new RNGCryptoServiceProvider();
                byteArray = new byte[4];
                provider.GetBytes(byteArray);

                //convert 4 bytes to an integer
                randomInteger = BitConverter.ToUInt32(byteArray, 0);
                temp += (((uint)randomInteger % 6) + 1) * ((uint)Math.Pow(10, (i - 1) % 5));
                if (i % 5 == 0)
                {
                    wordIndexes.Add(temp);
                    temp = 0;
                }
            }
            return wordIndexes;
        }

